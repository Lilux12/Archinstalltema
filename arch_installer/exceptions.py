"""Исключения установщика Arch Linux.

Иерархия пользовательских исключений для обработки ошибок
на всех этапах установки. Каждый тип исключения соответствует
определённой категории сбоя и позволяет оркестратору принимать
решение о дальнейших действиях (повтор, пропуск, выход).
"""

from __future__ import annotations


class InstallerError(Exception):
    """Базовое исключение установщика.

    Все остальные исключения наследуются от этого класса,
    что позволяет перехватывать любые ошибки установщика
    одним ``except InstallerError``.
    """


class PreflightError(InstallerError):
    """Ошибка предварительной проверки системы.

    Выбрасывается когда система не удовлетворяет минимальным
    требованиям для начала установки (нет интернета, недостаточно
    места, запуск не от root и т.д.).
    """


class StageError(InstallerError):
    """Ошибка выполнения этапа установки.

    Выбрасывается когда команда или этап завершается с ненулевым
    кодом возврата или иной ошибкой во время установки.

    Attributes:
        stage_name: Название этапа, на котором произошла ошибка.
        detail: Подробное описание ошибки (вывод команды, traceback и пр.).
    """

    def __init__(
        self,
        stage_name: str,
        detail: str,
        *args: object,
    ) -> None:
        self.stage_name: str = stage_name
        self.detail: str = detail
        message = f"[{stage_name}] {detail}"
        super().__init__(message, *args)


class ValidationError(InstallerError):
    """Ошибка валидации пользовательского ввода.

    Выбрасывается когда введённые пользователем данные не проходят
    проверку (имя пользователя, пароль, часовой пояс и т.д.).
    """


class UserAbort(InstallerError):
    """Пользователь отменил установку.

    Выбрасывается когда пользователь явно прерывает процесс
    установки через интерфейс (Ctrl+C, кнопка отмены).
    """
